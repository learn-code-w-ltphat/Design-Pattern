package HomeSystem;

public class Security {
    public void activateAlarm() {
        System.out.println("Security Alarm is activated");
    }

    public void deactivateAlarm() {
        System.out.println("Security Alarm is deactivated");
    }

    public void lockDoors() {
        System.out.println("Doors are locked");
    }

    public void unlockDoors() {
        System.out.println("Doors are unlocked");
    }

    public void turnOnCameras() {
        System.out.println("Cameras are ON");
    }

    public void turnOffCameras() {
        System.out.println("Cameras are OFF");
    }
}
